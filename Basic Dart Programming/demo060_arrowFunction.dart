int add(int num1, int num2) => num1 + num2;
int sub(int num1, int num2) => num1 - num2;
int mul(int num1, int num2) => num1 * num2;

void main() {
  print("Result of Addition : ${add(10, 20)}");
  print("Result of Subtraction : ${sub(10, 20)}");
  print("Result of Multiplication : ${mul(10, 20)}");   
}