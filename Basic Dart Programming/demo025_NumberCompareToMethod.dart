// compareTo()
// 0  : if the values are equal.
// 1  : if the current number object is greater than
// -1 : if the current number object is less than


void main() { 
  var myvar = 5;     // current number
  
  print(myvar.compareTo(5));
  print(myvar.compareTo(3));
  print(myvar.compareTo(7));   
} 